# Task 1: Using a for loop to iterate through numbers 0 to 10
for number in range(11):  # The range(11) generates numbers from 0 to 10
    # Task 2: Multiply the number by 2 and by 10
    multiplied_by_2 = number * 2
    multiplied_by_10 = number * 10
    
    # Print the results
    print(f"Number: {number}")
    print(f"Multiplied by 2: {multiplied_by_2}")
    print(f"Multiplied by 10: {multiplied_by_10}")
    print()  # Print a blank line for better readability
